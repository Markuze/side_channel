void InitializeKeyAnalysis(){

	int i;

	for(i = 0; i < 12; i++){
		SumBin0[i] = 0;
		SumBin1[i] = 0;
	}

	CountBin0 = 0;
	CountBin1 = 0;
}

void UpdateBin0(int cipherCount){

	int i;
	for(i = 0; i < 12; i++){
		SumBin0[i] += Sample[cipherCount][i];
	}
	CountBin0+= FreqSample[cipherCount];
} 

void UpdateBin1(int cipherCount){

	int i;
	for(i = 0; i < 12; i++){
		SumBin1[i]+= Sample[cipherCount][i];
	}
	CountBin1+= FreqSample[cipherCount];
}

void GetKeyBias(int keyVal){

	int i,j;
	double biasVal, meanDiff;
	int index;

	biasVal = 0.0;
	index = 0;

	for(i = 0; i < 12; i++){
		
		meanDiff = (double)SumBin1[i]/CountBin1 - (double)SumBin0[i]/CountBin0;
		if(meanDiff < 0.0){
			meanDiff = -1.0 * meanDiff;
		}
		if(biasVal < meanDiff){
			biasVal = meanDiff;
			index = i;
		}
	}
	BiasKey[keyVal] = biasVal;
	BiasIndex[keyVal] = index;

}

void CipherTextKeyAnalysis(int keyVal, int cipherCount){

	int targetByteVal, targetByte;

	InvLastRound();
	targetByte = GetTargetByte(TARGETBYTE);
	targetByteVal = (int)(out[targetByte]);
	// printf("%d %d\n",targetByteVal, cipherCount);
	if(targetByteVal % 2 == 1){
		UpdateBin1(cipherCount);
	}
	else{
		UpdateBin0(cipherCount);
	}
	// printf("%d %d\n",targetByteVal, cipherCount);

}

